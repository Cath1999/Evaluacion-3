    private void jbSumaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbSumaActionPerformed

        
        int n1, n2, total;
        n1=Integer.parseInt(this.jTexto1.getText());
        n2=Integer.parseInt(this.jTexto2.getText());
        
        total = n1 + n2;
        
        JOptionPane.showMessageDialog(null, total, "La suma es: ", JOptionPane.INFORMATION_MESSAGE);
            
        
    }//GEN-LAST:event_jbSumaActionPerformed

