    private void jbMenorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbMenorActionPerformed
 
        int n1, n2, menor;
        n1=Integer.parseInt(this.jTexto1.getText());
        n2=Integer.parseInt(this.jTexto2.getText());
        
        if(n1 < n2){
            menor = n1;
            JOptionPane.showMessageDialog(null, menor, "El menor es número 1:", JOptionPane.INFORMATION_MESSAGE);
        }
        else{
            menor = n2;
            JOptionPane.showMessageDialog(null, menor, "El menor es el número 2:", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_jbMenorActionPerformed

    private void jbIgualdadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbIgualdadActionPerformed

        int n1, n2;
        n1=Integer.parseInt(this.jTexto1.getText());
        n2=Integer.parseInt(this.jTexto2.getText());
        
        if(n1 == n2){
            JOptionPane.showMessageDialog(null, "Los números son iguales");
        }
        else{
            JOptionPane.showMessageDialog(null, "Los números no son iguales");
        }
    }//GEN-LAST:event_jbIgualdadActionPerformed

